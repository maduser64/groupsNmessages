     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $group = Group::find($request->group);
        $isInGroup = Auth::user()->isInGroup($group);
        
        return view('group', [
            'group' => $group,
            'userIsInGroup' => $isInGroup,
        ]);
    }

